    #Boolean parameter, 1 if flight i arrives or deperatures in time window s, and otherwise 0
    Alpha_is = {(i, s): 1 if s <= F[i].arrival_time < s + 15 or s <= F[i].departure_time < s + 15 else 0 for i in real_flight_ids for s in S_r}

    #upper limit of number of available gates for gate type k
    e_k = {k: len(H_k[k]) for k in K}

    #Time interval treshold between aircraft at a gate
    ksi = {}
    for i in F:
        if F[i].arrival_destination == "domestic" and F[i].departure_destination == "domestic":
            ksi[i] = 30
        else:
            ksi[i] = 100
    
   



    #TODO: Capacity limit of apron w at time u, now set for 30
    N_w_tau = 30

    #Maximum number of flights allowed on the runway during time window s
    mu_sgamma = {(s, gamma): 7 for s in S_r for gamma in Lambda}  

    #taxiway lengths in meters, keyed by taxiway name
    taxi_lengths_df = pd.read_excel("Taxiway_lengths.xlsx")
    taxi_lengths = dict(zip(taxi_lengths_df["Taxiway"].astype(str).str.strip(), taxi_lengths_df["Length_(m)"]))

    #average taxi speed in m/min (≈ 10 m/s, ~19 knots)
    taxi_speed = 600

    #routes()[idx] is the route list for a runway: 1=19L dep, 2=20R dep, 3=19R arr, 4=20L arr
    route_data = routes()
    runway_to_idx = {1: 0, 2: 1, 3: 2, 4: 3}

    #taxiing time from arrival runway γ to gate type k for flight i
    TA = {}
    for fid in real_flight_ids:
        gamma = F[fid].arrival_runway
        if gamma not in runway_to_idx:
            continue
        for k in K:
            corridor = k[4]
            if 1 <= corridor <= len(route_data[runway_to_idx[gamma]]):
                route = route_data[runway_to_idx[gamma]][corridor - 1]
                TA[fid, k, gamma] = sum(taxi_lengths.get(t, 0) for t in route) / taxi_speed

    #taxiing time from gate type k to departure runway γ for flight i
    TD = {}
    for fid in real_flight_ids:
        for k in K:
            corridor = k[4]
            for gamma in Lambda_i[fid]:
                if gamma in runway_to_idx and 1 <= corridor <= len(route_data[runway_to_idx[gamma]]):
                    route = route_data[runway_to_idx[gamma]][corridor - 1]
                    TD[fid, k, gamma] = sum(taxi_lengths.get(t, 0) for t in route) / taxi_speed

    #Parking time windows based on departure and arrival times
    t_A_ik = {}  # start of parking window for flight i at gate type k
    t_D_ik = {}  # end of parking window for flight i at gate type k
    for fid in F:
        for k in K:
            if fid in real_flight_ids:
                ta = TA.get((fid, k, F[fid].arrival_runway), float('inf'))
                td = min(TD.get((fid, k, gamma), float('inf')) for gamma in Lambda_i[fid])
            else:
                ta = 0.0
                td = 0.0
            t_A_ik[fid, k] = F[fid].arrival_time + ta
            t_D_ik[fid, k] = F[fid].departure_time - td

    return {
        "F": F, "G": G, "A": A, "D": D,
        "W": W, "K": K, "Lambda": Lambda,
        "S_w": S_w, "S_r": S_r,
        "H_k": H_k,
        "F_s_gamma_A": F_s_gamma_A,
        "F_s_D": F_s_D,
        "F_k": F_k,
        "Lambda_i": Lambda_i,
        "chi_kw": chi_kw,
        "l_k": l_k,
        "Alpha_is": Alpha_is,
        "e_k": e_k,
        "ksi": ksi,
        "N_w_tau": N_w_tau,
        "mu_sgamma": mu_sgamma,
        "NE_i": NE_i,
        "FF_i": FF_i,
        "f_i": f_i,
        "TA": TA,
        "TD": TD,
        "t_A_ik": t_A_ik,
        "t_D_ik": t_D_ik,
        "virtual_0": virtual_0,
        "virtual_n1": virtual_n1,
        "real_flight_ids": real_flight_ids,
        "overlaps_set": overlaps_set,
    }


